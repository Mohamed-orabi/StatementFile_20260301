        private static BankProductConfig Map(IDataReader r)
        {
            int id = r.GetInt32(0);

            var config = BankProductConfig.Create(
                branchCode:                  r.GetInt32(1),
                bankName:                    r.IsDBNull(2)  ? string.Empty : r.GetString(2),
                bankFullName:                r.IsDBNull(3)  ? string.Empty : r.GetString(3),
                cardProduct:                 r.IsDBNull(4)  ? string.Empty : r.GetString(4),
                outputType:                  (StatementOutputType)r.GetInt32(5),
                formatterKey:                r.IsDBNull(6)  ? string.Empty : r.GetString(6),
                processingModes:             (ProcessingMode)r.GetInt64(7),
                cardType:                    (CardType)r.GetInt32(8),
                statementType:               (StatementType)r.GetInt32(9),
                statementTypeSuffix:         r.IsDBNull(10) ? "CR"          : r.GetString(10),
                rewardContractCondition:     r.IsDBNull(11) ? null           : r.GetString(11),
                installmentContractCondition:r.IsDBNull(12) ? null           : r.GetString(12),
                appendMode:                  r.GetInt32(13) == 1,
                useCorporateAccountNumber:   r.GetInt32(14) == 1,
                reportTemplateName:          r.IsDBNull(15) ? null           : r.GetString(15),
                pdfPasswordScheme:           r.IsDBNull(16) ? null           : r.GetString(16),
                emailFromAddress:            r.IsDBNull(17) ? null           : r.GetString(17),
                bankWebsiteUrl:              r.IsDBNull(18) ? null           : r.GetString(18),
                facebookUrl:                 r.IsDBNull(19) ? null           : r.GetString(19),
                linkedInUrl:                 r.IsDBNull(20) ? null           : r.GetString(20),
                youTubeUrl:                  r.IsDBNull(21) ? null           : r.GetString(21),
                maxTransactionsPerPage:      r.GetInt32(22),
                maxTransactionsLastPage:     r.GetInt32(23),
                emailWaitPeriodMs:           r.GetInt32(24),
                fieldSeparator:              r.IsDBNull(25) ? ","            : r.GetString(25),
                scheduledDay:                r.GetInt32(26),
                displayName:                 r.IsDBNull(27) ? null           : r.GetString(27),
                sortOrder:                   r.GetInt32(28),
                isActive:                    r.GetInt32(29) == 1);

            return BankProductConfig.Reconstitute(id, config);
        }

        private static object NullIfEmpty(string s)
            => string.IsNullOrEmpty(s) ? (object)DBNull.Value : s;
    }
