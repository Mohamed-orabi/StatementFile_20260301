    /// <summary>
    /// Oracle implementation of <see cref="IBankProductConfigRepository"/>.
    ///
    /// Reads/writes the STAT_BANK_PRODUCT_CONFIG table, which replaces the
    /// hundreds of hardcoded bank entries previously in frmStatementFileExtn.cs.
    ///
    /// All mutable fields of <see cref="BankProductConfig"/> are persisted.
    /// The primary key is generated by the Oracle sequence STAT_BPCFG_SEQ.
    /// </summary>
    public sealed class BankProductConfigRepository : IBankProductConfigRepository
    {
        private readonly OracleConnectionFactory _connFactory;

        public BankProductConfigRepository(OracleConnectionFactory connFactory)
        {
            _connFactory = connFactory ?? throw new ArgumentNullException(nameof(connFactory));
        }

        // ── SELECT helpers ────────────────────────────────────────────────────────

        private const string SelectColumns = @"
            ID, BRANCH_CODE, BANK_NAME, BANK_FULL_NAME, CARD_PRODUCT,
            OUTPUT_TYPE, FORMATTER_KEY, PROCESSING_MODES,
            CARD_TYPE, STATEMENT_TYPE, STATEMENT_TYPE_SUFFIX,
            REWARD_CONTRACT_COND, INSTALLMENT_CONTRACT_COND,
            APPEND_MODE, USE_CORP_ACCT_NO,
            REPORT_TEMPLATE_NAME, PDF_PASSWORD_SCHEME,
            EMAIL_FROM_ADDRESS, BANK_WEBSITE_URL,
            FACEBOOK_URL, LINKEDIN_URL, YOUTUBE_URL,
            MAX_TRANS_PER_PAGE, MAX_TRANS_LAST_PAGE,
            EMAIL_WAIT_MS, FIELD_SEPARATOR,
            SCHEDULED_DAY, DISPLAY_NAME, SORT_ORDER, IS_ACTIVE,
            CREATED_AT, MODIFIED_AT";

        private const string TableName = "STAT_BANK_PRODUCT_CONFIG";

        public IReadOnlyList<BankProductConfig> GetAll()
        {
            var list = new List<BankProductConfig>();
            using var conn = _connFactory.CreateOpenConnection();
            using var cmd  = new OracleCommand(
                $"SELECT {SelectColumns} FROM {TableName} ORDER BY SORT_ORDER, BRANCH_CODE", conn);
            using var rdr  = cmd.ExecuteReader();
            while (rdr.Read())
                list.Add(Map(rdr));
            return list;
        }

        public IReadOnlyList<BankProductConfig> GetActive()
        {
            var list = new List<BankProductConfig>();
            using var conn = _connFactory.CreateOpenConnection();
            using var cmd  = new OracleCommand(
                $"SELECT {SelectColumns} FROM {TableName} WHERE IS_ACTIVE = 1 ORDER BY SORT_ORDER, BRANCH_CODE", conn);
            using var rdr  = cmd.ExecuteReader();
            while (rdr.Read())
                list.Add(Map(rdr));
            return list;
        }

        public BankProductConfig GetById(int id)
        {
            using var conn = _connFactory.CreateOpenConnection();
            using var cmd  = new OracleCommand(
                $"SELECT {SelectColumns} FROM {TableName} WHERE ID = :id", conn);
            cmd.Parameters.Add(new OracleParameter(":id", OracleDbType.Int32) { Value = id });
            using var rdr  = cmd.ExecuteReader();
            return rdr.Read() ? Map(rdr) : null;
        }

        public int Insert(BankProductConfig config)
        {
            using var conn = _connFactory.CreateOpenConnection();
            using var cmd  = new OracleCommand(@"
                INSERT INTO STAT_BANK_PRODUCT_CONFIG (
                    ID, BRANCH_CODE, BANK_NAME, BANK_FULL_NAME, CARD_PRODUCT,
                    OUTPUT_TYPE, FORMATTER_KEY, PROCESSING_MODES,
                    CARD_TYPE, STATEMENT_TYPE, STATEMENT_TYPE_SUFFIX,
                    REWARD_CONTRACT_COND, INSTALLMENT_CONTRACT_COND,
                    APPEND_MODE, USE_CORP_ACCT_NO,
                    REPORT_TEMPLATE_NAME, PDF_PASSWORD_SCHEME,
                    EMAIL_FROM_ADDRESS, BANK_WEBSITE_URL,
                    FACEBOOK_URL, LINKEDIN_URL, YOUTUBE_URL,
                    MAX_TRANS_PER_PAGE, MAX_TRANS_LAST_PAGE,
                    EMAIL_WAIT_MS, FIELD_SEPARATOR,
                    SCHEDULED_DAY, DISPLAY_NAME, SORT_ORDER, IS_ACTIVE,
                    CREATED_AT, MODIFIED_AT
                ) VALUES (
                    STAT_BPCFG_SEQ.NEXTVAL,
                    :branchCode, :bankName, :bankFullName, :cardProduct,
                    :outputType, :formatterKey, :processingModes,
                    :cardType, :statementType, :stmtTypeSuffix,
                    :rewardCond, :installmentCond,
                    :appendMode, :useCorpAcct,
                    :reportTemplate, :pdfPwdScheme,
                    :emailFrom, :bankWebsite,
                    :facebookUrl, :linkedInUrl, :youTubeUrl,
                    :maxTransPage, :maxTransLast,
                    :emailWaitMs, :fieldSep,
                    :scheduledDay, :displayName, :sortOrder, :isActive,
                    :createdAt, :modifiedAt
                ) RETURNING ID INTO :newId", conn);

            AddWriteParams(cmd, config);
            var newId = new OracleParameter(":newId", OracleDbType.Int32)
                { Direction = ParameterDirection.Output };
            cmd.Parameters.Add(newId);

            cmd.ExecuteNonQuery();
            return Convert.ToInt32(newId.Value.ToString());
        }

